class GetSim(nn.Module):
    def __init__(self):
        super(GetSim, self).__init__()

    def forward(self, x_dis, x_ref):
        assert x_dis.size() == x_ref.size()

        N, C, H, W = x_dis.size()
        x_dis = x_dis.view(N, C, H * W)
        x_ref = x_ref.view(N, C, H * W)

        # x_mul = torch.cosine_similarity(x_dis, x_ref, dim=2, eps=1e-9)

        return torch.cosine_similarity(x_dis, x_ref, dim=2, eps=1e-9)


